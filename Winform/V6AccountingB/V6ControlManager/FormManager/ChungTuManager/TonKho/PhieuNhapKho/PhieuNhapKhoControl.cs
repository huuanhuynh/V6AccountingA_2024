                                // Không Có vị trí chỉ định
                                newData["MA_VITRI"] = data_row["MA_VITRI"];
                            }
                            newData["MA_KHO_I"] = data_row["MA_KHO"];
                            if (temp_vt.VITRI_YN) newData["MA_VITRI"] = data_row["MA_VITRI"];

                            newData["DVT"] = dvt;
                            newData["DVT1"] = dvt1;
                            newData["HE_SO"] = heso;
